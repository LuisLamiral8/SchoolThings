<?php

function sumar($num1, $num2) {
    $suma = $num1 + $num2;
    return $suma;
}
function restar($num1, $num2) {
    $resta = $num1 - $num2;
    return $resta;
}
function multi($num1, $num2) {
    $multi = $num1 * $num2;
    return $multi;
}
function divi($num1, $num2) {
    $divi = $num1 / $num2;
    return $divi;
}

